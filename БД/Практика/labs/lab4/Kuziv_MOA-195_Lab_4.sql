-- Задание 8
SELECT P.NAME, COUNT(C.ID)
FROM S_CUSTOMER C RIGHT JOIN S_ORD O
ON C.ID = O.CUSTOMER_ID
RIGHT JOIN S_ITEM I
ON I.ORD_ID = O.ID
RIGHT JOIN S_PRODUCT P
ON P.ID = I.PRODUCT_ID
GROUP BY P.NAME;

