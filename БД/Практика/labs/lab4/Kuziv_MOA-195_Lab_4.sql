-- Задание 5
SELECT C.NAME, O.ID, COUNT(O.ID)
FROM S_CUSTOMER C INNER JOIN S_ORD O
ON O.CUSTOMER_ID = C.ID
WHERE O.TOTAL > 100000
GROUP BY C.NAME, O.ID;

-- Задание 6
SELECT R.ID, R.NAME, D.NAME, COUNT(E.ID)
FROM S_REGION R FULL JOIN S_DEPT D 
ON R.ID = D.REGION_ID
FULL JOIN S_EMP E
ON E.DEPT_ID = D.ID
GROUP BY R.ID, R.NAME, D.NAME;


